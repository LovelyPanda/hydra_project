#ifndef UNKNOWN_MODEL_FILE_EXCEPTION_H__
#define UNKNOWN_MODEL_FILE_EXCEPTION_H__

#include <stdexcept>

namespace hydra{

namespace loading{

class UnknownModelFileException: public std::runtime_error{

public:
	UnknownModelFileException(const std::string& file): std::runtime_error("Unknown model file: "+file){}
	~UnknownModelFileException() throw() {}

	virtual const char* what() const throw() {
		return std::runtime_error.what();
	}
		
};

} //loading namespace

} //hydra

#endif

